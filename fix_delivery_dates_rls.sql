-- ============================================
-- ИСПРАВЛЕНИЕ: Разрешить чтение дат доставки
-- ============================================
-- 
-- ПРОБЛЕМА: Таблица delivery_dates создана с RLS, но нет политики для чтения
-- РЕШЕНИЕ: Создаем политику, которая разрешает всем читать даты доставки
-- 
-- ИНСТРУКЦИЯ:
-- 1. Откройте SQL Editor в Supabase
-- 2. Скопируйте и выполните этот запрос
-- 3. После этого даты доставки начнут отображаться
-- 
-- ============================================

-- Создаем политику: все могут читать даты доставки (anon роль)
CREATE POLICY "Allow public read access to delivery_dates"
ON delivery_dates
FOR SELECT
TO anon, authenticated
USING (true);

